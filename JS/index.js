function menubar()
{
    document.getElementById("menubar").classList.add("showmenubar");
    document.getElementById("sidelinks").classList.add("showsidelinks");
}
function hidemenubar()
{
    document.getElementById("menubar").classList.remove("showmenubar");
    document.getElementById("sidelinks").classList.add("showsidelinks");
}
